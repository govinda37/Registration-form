    public void fillStudentTable() {
        Object colums[] = {"Id","S Name","Roll No","Email","Fees","U Name","Depar","Gender"};
        DefaultTableModel model = new DefaultTableModel(colums, 0);
        List<Student> list = studentDao.getAllStudent();
        for (Student student : list) {
            Object rowAdd[] = {student.getId(),student.getStudentName(),student.getRollNo(),student.getEmail(),student.getFees(),student.getuName(),student.getDepartment(),student.getGender()};
            model.addRow(rowAdd);
            StudentTable2.setModel(model);
        }
    }


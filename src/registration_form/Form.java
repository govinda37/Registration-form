    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String name = nameText.getText();
        String sRollNo = rollText.getText();
        Integer rollNo = Integer.parseInt(sRollNo);
        String email = emailText.getText();
        String sFees = fessText.getText();
        Integer fees = Integer.parseInt(sFees);
        String uName = unameText.getText();
        String department = (String) departmentText.getSelectedItem();
        String gender = "";
        if (maleRadio.isSelected()) {
            gender = maleRadio.getText();
        } else {
            gender = femaleRadio.getText();
        }
        s.setDepartment(department);
        s.setEmail(email);
        s.setFees(fees);
        s.setGender(gender);
        s.setRollNo(rollNo);
        s.setStudentName(name);
        s.setuName(uName);
        Integer roll = studentDao.addStudent(s);
        if (roll>= 0) {
            System.out.println("Addedd Successfully");
            fillStudentTable();
        }
    }//GEN-LAST:event_addButtonActionPerformed

